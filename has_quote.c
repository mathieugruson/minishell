#include "minishell.h"

int	has_quote(char *str)
{
	int	i;
	int	s;
	int	d;

	i = 0;
	s = 0;
	d = 0;
	while (str[i])
	{
		if (str[i] == 34)
			d++;
		else if (str[i] == 39)
			s++;
		i++;
	}
	if (d + s >= 2)
		return (1);
	return (0);
}
